function get_language_by_category($postid){
	$wp_active_languages = get_active_languages();
	$categories = get_the_category($postid);
	foreach($categories as $category){
		if( array_key_exists($category->name, $wp_active_languages) ){
			return $wp_active_languages[$category->name];
		}
	}
	return null;
}

function get_language_by_page($page_id){
	$wp_active_languages = get_active_languages();
	$parent_post_id = wp_get_post_parent_id($page_id);
	
	foreach($wp_active_languages as $k=>$v){
		if($v['code'] == $page_id || $v['code'] == $parent_post_id){
			return $wp_active_languages[$k];
		}
	}
	return null;
}

function get_language_array($language, $code_only = false){
	return $code_only ? array('LANG'=>$language['code'], 'CODE'=>$language['index']) : $language;
}

function get_current_language($code_only = false){	
	$wp_active_languages = get_active_languages();
	if( defined('CURRENT_PAGE_ID') ){
		$post = get_post(CURRENT_PAGE_ID);
	}else{
		$post = get_post();
	}
	if( $post === null){
		$en = $wp_active_languages['en']; // english as default
		return $code_only ? array('LANG'=>$en['code'], 'CODE'=>'en') : $en;
	}
	$language = get_language_by_category($post->ID);
	if($language !== null){
		return get_language_array($language, $code_only);
	}
	$language = get_language_by_page($post->ID);
	if($language !== null){
		return get_language_array($language, $code_only);
	}
	throw new LanguageNotDetectedException('Language code not found!');
}
